def print_max(a, b, c) : #def를 사용하여 print_max(a, b, c)의 a,b,c값을 받음
    if a > b and a > b: #a가 최고값일때 a출력
        print(a)
    elif b > a and b > c: #b가 최고값일때 b출력
        print(b)
    else:             #a,b가 최고값이 아닐때 c가 최고값임으로 c출력
        print(c)
print_max(1,2,3) #a,b,c값 대입


